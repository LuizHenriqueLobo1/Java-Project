package exe14;

public class Ponto {

	private float x;
	private float y;
	
	public Ponto() {
		this.x = 0;
		this.y = 0;
	}
	
	public Ponto(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public float getX() {
		return this.x;
	}
	
	public float getY() {
		return this.y;
	}
	
	public void setX(float x) {
		this.x = x;
	}
	
	public void setY(float y) {
		this.y = y;
	}
	
}
